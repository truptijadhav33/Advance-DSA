public class BasicOperations {

    public static void main(String[] args) {
        int a=5<<1;
        System.out.println("5 << 1 ( left shift ): " + a);
        int b=5>>1;
        System.out.println("5 >> 1 ( right shift ): " + b);
        int c=5;
        System.out.println("~5: "+ ~c);

    }
}